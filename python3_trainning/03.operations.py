#coding: UTF-8

# ---------------------------------------
# @note 四則演算
# - // は小数切り捨て
# - % は剰余
# ---------------------------------------

# 加算
a = 7
b = 3
c = a + b
print(c)

# 減算
d = a - b
print(d)

# 乗算
e = a * b
print(e)

# 除算(結果小数)
f = a / b
print(f)

# 結果整数
g = a // b
print(g)

# 剰余
h = a % b
print(h)

i = a + 3
print(i)

a = a + 3
a += 3
print(a)
