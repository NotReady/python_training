# リスト
a = [2014, 2015, 2016]
# タプル
b = (2014, 2015, 2016)

print(a)
print(b)

# 参照式は同じ
print(a[1])
print(b[1])

a[1] = 2017
print(a)

# タプルは上書き不可
# b[1] = 2017
# print(b)

# 要素の追加
