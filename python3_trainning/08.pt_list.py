# ---------------------------------------
# @note リスト
# - 異なる型も一緒に格納できる
# - [from:to]区で範囲指定して取り出すことができる
# ---------------------------------------

# 定義
a = [2014, 2015, 2016]
print(a)

# 要素を追加
a.append(2018)
print(a)

# 要素の参照
print(a[0])
print(a[1])
print(a[2])

# 要素の型はなんでもあり
b = 12
c = [b, 2017, 20.1, "Hello", "Hi"]
# 範囲指定子で範囲指定参照
print(c[1:4])