# 整数
a = 123
print(a)

b = a
print(b)

# 小数
c = 123.4
print(c)

d = c
print(d)

# 文字列
e = "こんにちは"
print(e)

f = e
print(f)

# 真偽値
g = True
print(g)

h = False
print(h)

i = g
print(i)